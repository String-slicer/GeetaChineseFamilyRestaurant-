// Helper to map raw items into standard dish objects
const mapItems = (items, isVeg) => {
  return items.map((item, idx) => {
    const name = item.item_name;
    const category = item.category;

    // Deterministic rating between 4.4 and 4.9 based on name
    const rating = 4.4 + (name.charCodeAt(0) % 6) * 0.1;
    // Deterministic reviews count
    const reviews = 50 + (name.charCodeAt(1) % 15) * 12;

    // Dynamic descriptive generation based on category/spiciness
    let description = "";
    if (category === "Soup") {
      description = `A comforting bowl of traditional ${name.toLowerCase()} slow-simmered with premium aromatic herbs and chef's broth.`;
    } else if (category === "Starter") {
      description = `Popular wok-fired starter featuring ${name.toLowerCase()} seasoned with dry chilies, garlic, and fresh scallions.`;
    } else if (category === "Rice") {
      description = `Premium quality long-grain rice wok-tossed with ${name.toLowerCase()}, light soy sauce, and handpicked garden veggies.`;
    } else if (category === "Noodles") {
      description = `Authentic Chinese stir-fried noodles tossed with ${name.toLowerCase()} and crisp garden juliennes.`;
    }

    // Dynamic tags
    const tags = [];
    if (name.includes("Schezwan") || name.includes("Chilli") || name.includes("Manchow") || name.includes("Hot")) {
      tags.push("Spicy");
    }
    if (name.includes("Crispy") || name.includes("Dry") || name.includes("Lolipop") || name.includes("Bhel")) {
      tags.push("Crispy");
    }
    if (name.includes("Triple") || name.includes("Chopper") || name.includes("Paneer Crispy") || name.includes("Royal")) {
      tags.push("Chef's Special");
    }
    if (name.includes("Garlic") || name.includes("Ginger") || name.includes("Lemon")) {
      tags.push("Aromatic");
    }

    // Static image mapping based on category to maintain high aesthetics
    let image = "/hero_dish.png";
    if (category === "Soup") {
      image = "/soup.png";
    } else if (category === "Starter") {
      image = "/szechuan_chicken.png";
    } else if (category === "Noodles") {
      image = "/noodles.png";
    } else if (category === "Rice") {
      image = "/fried_rice.png";
    }

    return {
      id: `${isVeg ? "V" : "NV"}-${category.substring(0, 2)}-${idx + 1}`,
      name: name,
      description: description,
      category: category,
      isVeg: isVeg,
      rating: parseFloat(rating.toFixed(1)),
      reviews: reviews,
      image: image,
      hasHalfOption: item.half_price !== null,
      priceFull: item.full_price,
      priceHalf: item.half_price,
      tags: tags.slice(0, 2) // Limit to max 2 tags for card size consistency
    };
  });
};

const allMappedItems = [
  ...mapItems(rawVegItems, true),
  ...mapItems(rawNonVegItems, false)
];

// Order categorization rules
const categoryRanking = {
  "Starter": 1,
  "Soup": 2,
  "Noodles": 3,
  "Rice": 4
};

// Sort all menu items so starters are loaded at the absolute top by default
export const menuItems = allMappedItems.sort((a, b) => {
  return categoryRanking[a.category] - categoryRanking[b.category];
});
